import { RecurringTransaction } from "@/types";
import { clsx, type ClassValue } from "clsx";
import { twMerge } from "tailwind-merge";
import {
  addDays,
  addMonths,
  addWeeks,
  addYears,
  nextDay,
  setDate,
} from "date-fns";
import { supabase } from "@/supabase/client";

const BUCKET_NAME = process.env.NEXT_PUBLIC_BUCKET_NAME || "blog-assets";

export function getStorageUrl(filePath: string | null | undefined): string {
  if (!filePath) return "";
  if (filePath.startsWith("http")) return filePath;

  const { data } = supabase.storage.from(BUCKET_NAME).getPublicUrl(filePath);
  return data.publicUrl;
}

export function cn(...inputs: ClassValue[]): string {
  return twMerge(clsx(inputs));
}

export function formatDate(
  dateInput: Date | string | number,
  options?: Intl.DateTimeFormatOptions,
): string {
  const date = new Date(dateInput);
  const defaultOptions: Intl.DateTimeFormatOptions = {
    year: "numeric",
    month: "long",
    day: "numeric",
    ...options,
  };
  return date.toLocaleDateString(undefined, defaultOptions);
}

export function slugify(text: string): string {
  if (!text) return "";
  return text
    .toString()
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-")
    .replace(/[^\w-]+/g, "")
    .replace(/--+/g, "-")
    .replace(/^-+/, "")
    .replace(/-+$/, "");
}

export const getNextOccurrence = (
  cursor: Date,
  rule: RecurringTransaction,
): Date => {
  let next = new Date(cursor);
  switch (rule.frequency) {
    case "daily":
      return addDays(next, 1);
    case "weekly":
      return rule.occurrence_day !== null && rule.occurrence_day !== undefined
        ? nextDay(next, rule.occurrence_day as any)
        : addWeeks(next, 1);
    case "bi-weekly":
      return rule.occurrence_day !== null && rule.occurrence_day !== undefined
        ? nextDay(addWeeks(next, 1), rule.occurrence_day as any)
        : addWeeks(next, 2);
    case "monthly":
      next = addMonths(next, 1);
      return rule.occurrence_day ? setDate(next, rule.occurrence_day) : next;
    case "yearly":
      return addYears(next, 1);
    default:
      return addDays(next, 1);
  }
};

export function hexToHsl(hex: string): string {
  let r = 0,
    g = 0,
    b = 0;
  if (hex.length === 4) {
    r = parseInt("0x" + hex[1] + hex[1]);
    g = parseInt("0x" + hex[2] + hex[2]);
    b = parseInt("0x" + hex[3] + hex[3]);
  } else if (hex.length === 7) {
    r = parseInt("0x" + hex[1] + hex[2]);
    g = parseInt("0x" + hex[3] + hex[4]);
    b = parseInt("0x" + hex[5] + hex[6]);
  }
  r /= 255;
  g /= 255;
  b /= 255;
  const cmin = Math.min(r, g, b),
    cmax = Math.max(r, g, b),
    delta = cmax - cmin;
  let h = 0,
    s = 0,
    l = 0;

  if (delta === 0) h = 0;
  else if (cmax === r) h = ((g - b) / delta) % 6;
  else if (cmax === g) h = (b - r) / delta + 2;
  else h = (r - g) / delta + 4;

  h = Math.round(h * 60);
  if (h < 0) h += 360;
  l = (cmax + cmin) / 2;
  s = delta === 0 ? 0 : delta / (1 - Math.abs(2 * l - 1));
  s = +(s * 100).toFixed(1);
  l = +(l * 100).toFixed(1);

  return `${h} ${s}% ${l}%`;
}